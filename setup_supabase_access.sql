-- 1. GARANTIR QUE A TABELA DE PROFESSORES AUTORIZADOS EXISTE (COM CAMPO NOME E ROLE)
CREATE TABLE IF NOT EXISTS public.authorized_professors (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    nome TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'professor',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. ADICIONAR COLUNA ROLE SE NÃO EXISTIR (Para compatibilidade)
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT FROM information_schema.columns WHERE table_name='authorized_professors' AND column_name='role') THEN
        ALTER TABLE public.authorized_professors ADD COLUMN role TEXT NOT NULL DEFAULT 'professor';
    END IF;
END $$;

-- 3. HABILITAR RLS NA TABELA
ALTER TABLE public.authorized_professors ENABLE ROW LEVEL SECURITY;

