-- 5. CONFIGURAR RLS PARA QUE APENAS GESTORES ACESSEM TUDO
-- (Usando o cargo 'gestor' definido na tabela authorized_professors)

-- Função auxiliar para verificar se é gestor (no DB)
CREATE OR REPLACE FUNCTION public.is_gestor()
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.authorized_professors 
    WHERE email = auth.jwt() ->> 'email' AND role = 'gestor'
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- POLÍTICA PARA A TABELA 'incidents'
DROP POLICY IF EXISTS "Managers can do everything" ON incidents;
CREATE POLICY "Managers can do everything"
  ON incidents FOR ALL
  TO authenticated
  USING (public.is_gestor())
  WITH CHECK (public.is_gestor());

-- POLÍTICA PARA A TABELA 'students'
DROP POLICY IF EXISTS "Managers can manage students" ON students;
CREATE POLICY "Universal students sync"
  ON students FOR ALL
  TO authenticated
  USING (true)
  WITH CHECK (true);

